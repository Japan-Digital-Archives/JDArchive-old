<p>最終的には、このプロジェクトは皆様の投稿により支えられており、その対象とするところは、私たちのスタッフが収集し管理する上記のような情報の範囲を大きく超えています。ご自身の経験されたことや皆様が重要と思われる情報を少しでも詳しく教えて頂けるよう、重ねて切にお願い申し上げます。</p>

